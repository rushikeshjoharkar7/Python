"""
Lambda function in python:

- A function which has no name and can intake any number of arguments
     and one expression is lambda function.

 - Lambda function reduces the number of lines of code to be written in a normal function.
"""
################################################################################################

sum = lambda a, b: a + b
print(sum(2, 3))

mul = lambda m : m ** 2
print(mul(21))

