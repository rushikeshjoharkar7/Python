# max() and min() are inbuilt funcitons


# for integers it takes max and min value
ma = max(12, 213214, 424, 2342, 234)
mi = min(12, 213214, 424, 2342, 234)

# for strings it sorts by alphabetical order
max_Str = max("orange", "green", "yellow", "blue")
min_Str = min("orange", "green", "yellow", "blue")

print(ma, mi, max_Str, min_Str)

